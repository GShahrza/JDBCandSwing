    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String header = txtHeader.getText();
            String beginDateStr = txtBeginDate.getText();
            String endDateStr = txtEndDate.getText();
            String jobDescription = txtAreaJobDescription.getText();
            
            Date beginDate = new Date(sdf.parse(beginDateStr).getTime());
            Date endDate = new Date(sdf.parse(endDateStr).getTime());
            EmploymentHistory emp = new EmploymentHistory(null,header,beginDate,endDate,jobDescription,new User(Config.loggedInUser.getId()));
            
            historyDao.addEmploymentHistory(emp);
            fillTable();
        } catch (ParseException ex) {
            Logger.getLogger(EmploymentHistoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int index = tblEmployment.getSelectedRow();
       EmploymentHistory emp = list.get(index);
       historyDao.deleteEmploymentHistory(emp.getId());
       fillTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


