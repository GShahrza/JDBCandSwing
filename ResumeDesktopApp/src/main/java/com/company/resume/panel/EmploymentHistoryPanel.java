    private void fillTable(){
        list = historyDao.getAllEmploymentHistoryByUserId(Config.loggedInUser.getId());
        System.out.println(list);
        Vector<Vector> rows = new Vector<>();
        for (EmploymentHistory l : list) {
            Vector row = new Vector();
            row.add(l.getHeader());
            row.add(l.getBeginDate());
            row.add(l.getEndDate());
            row.add(l.getJobDescription());
            rows.add(row);
        }
        DefaultTableModel model = new DefaultTableModel(rows, new Vector(Arrays.asList("Header","BeginDate","EndDate","JobDescription")));
        
        tblEmployment.setModel(model);
    }
    public void fillUserComponents(){
        fillTable();
    }
